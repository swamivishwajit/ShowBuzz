package com.extremity.showbuzz.core.domain;

public class MovieCategory {
	private Integer movieCategoryId;
	private String movieCategoryType;
	public Integer getMovieCategoryId() {
		return movieCategoryId;
	}
	public void setMovieCategoryId(Integer movieCategoryId) {
		this.movieCategoryId = movieCategoryId;
	}
	public String getMovieCategoryType() {
		return movieCategoryType;
	}
	public void setMovieCategoryType(String movieCategoryType) {
		this.movieCategoryType = movieCategoryType;
	}
}
